public class CWH_switch {
    public static void main(String[] args){
        String var  = "Saurabh";

        switch (var){
            case "Saurabh" -> {
                System.out.println("You are duffer");
            }
            case "Shubham" -> {
                System.out.println("You idiot");
            }
            default -> System.out.println("You are good");
        }
        System.out.println("Thanks for using my code");
    }
}